int	ft_str_is_alpha(char *str)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z') ||
					(str[i] >= 'A' && str[i] <= 'Z'))
			c++;
		i++;
	}
	if (i == c)
		return (1);
	else
		return (0);
}
