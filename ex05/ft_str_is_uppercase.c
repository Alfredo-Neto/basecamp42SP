int	ft_str_is_uppercase(char *str)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			c++;
		i++;
	}
	if (i == c)
		return (1);
	else
		return (0);
}
